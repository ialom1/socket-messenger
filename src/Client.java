    class MessageReader implements Runnable{

        @Override
        public void run() {
            while(true){
                readMessage();
            }
        }
        
        private void readMessage(){
            try {
                reader = new DataInputStream(socket.getInputStream());
                String temp = reader.readUTF();
                setMessage("Message from : " + socket.getPort());
                setMessage(temp + "\n");

            } catch (Exception e) {
                jLabel3.setText("Message could not be read");
                System.out.println("" + e);
            }
        }
        
    }    
    
