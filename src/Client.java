    private void receiveFile(String fileName){
        try {
            writer = new DataOutputStream(socket.getOutputStream());
            BufferedInputStream fileReader = new BufferedInputStream(socket.getInputStream());
            BufferedOutputStream fileWriter;            
            writer.writeUTF("file");
            writer.writeUTF(fileName);
            writer.flush();
            setMessage("File Requested : " + fileName);
                        
            int code = fileReader.read();
            
            if(code == 1){
                fileWriter = new BufferedOutputStream(new FileOutputStream("NEW" + fileName));
                
                byte[] buffer = new byte[1024];
                int byteRead = 0;
                
                while((byteRead = fileReader.read(buffer)) != -1){
                    fileWriter.write(buffer, 0, byteRead);
                    fileWriter.flush();                    
                }
                fileWriter.close();
                setMessage("File Received\n");
            }else{
                setMessage("File not present on the Server\n");
            }
            
        } catch (Exception e) {
            jLabel3.setText("Message could not be sent");
            System.out.println("" + e);
        }
    }
    
    private void writeMessage(String message){
        try {
            writer = new DataOutputStream(socket.getOutputStream());
            writer.writeUTF(message);
            writer.flush();
            setMessage("Message To : " + socket.getPort());
            setMessage(message + "\n");
            
        } catch (Exception e) {
            jLabel3.setText("Message could not be sent");
            System.out.println("" + e);
        }
    }
    
    private void openClientSocket(String serverIP, int serverPort){
        try {
            socket = new Socket(serverIP, serverPort);
            jLabel3.setText("Connected to server");
            Thread t = new Thread(new MessageReader());
            t.start();
            String username = JOptionPane.showInputDialog("Please provide a username");
            writeMessage(username);
        } catch (IOException ex) {
            jLabel3.setText("Connection Failed");
            System.out.println("" + ex);
        }
    }
    
    
    private void setMessage(String msg){
        jTextPane3.setText(jTextPane3.getText() + "\n" + msg);
    }
    
    
